export function createI18n(initialLanguage = "es") {
  let language = translations[initialLanguage] ? initialLanguage : "es";

  function setLanguage(nextLanguage) {
    if (!translations[nextLanguage]) return language;
    language = nextLanguage;
    return language;
  }

  function t(key, params = {}) {
    const table = translations[language] || translations.es;
    const fallback = translations.es[key] || key;
    const template = table[key] || fallback;
    return template.replace(/\{(\w+)\}/g, (_match, name) => String(params[name] ?? ""));
  }

  function getLanguage() {
    return language;
  }

  return {
    getLanguage,
    setLanguage,
    t
  };
}
